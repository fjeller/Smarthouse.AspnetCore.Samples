using System;
using System.Collections.Generic;
using System.Text;

namespace TodoSample.Core.Models
{
	/// =================================================================================================================
	/// <summary>
	/// The model for a todoitem list including the data to add a new todoitem
	/// </summary>
	/// =================================================================================================================
	public class TodoItemListModel
	{
		/// =================================================================================================================
		/// <summary>
		/// The text for a new item
		/// </summary>
		/// =================================================================================================================
		public string NewItemText { get; set; }

		/// =================================================================================================================
		/// <summary>
		/// The due date for a new item
		/// </summary>
		/// =================================================================================================================
		public string NewItemDueDate { get; set; }

		/// =================================================================================================================
		/// <summary>
		/// The currently available items
		/// </summary>
		/// =================================================================================================================
		public List<TodoItemModel> Items { get; set; }

	}
}
