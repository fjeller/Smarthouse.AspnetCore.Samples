using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace TagHelperSample.Models
{
	/// =================================================================================================================
	/// <summary>
	/// The model for the cookie consent tag helper
	/// </summary>
	/// =================================================================================================================
	public class AddressModel
	{
		/// =================================================================================================================
		/// <summary>
		/// The name of the person whose address should be rendered
		/// </summary>
		/// =================================================================================================================
		public string Name { get; set; }

		/// =================================================================================================================
		/// <summary>
		/// The street of the person whose address should be rendered
		/// </summary>
		/// =================================================================================================================
		public string Street { get; set; }

		/// =================================================================================================================
		/// <summary>
		/// The zip code of the city 
		/// </summary>
		/// =================================================================================================================
		public string ZipCode { get; set; }

		/// =================================================================================================================
		/// <summary>
		/// The name of the city the person lives in
		/// </summary>
		/// =================================================================================================================
		public string City { get; set; }

	}
}
